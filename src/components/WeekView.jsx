// Sub-component: Meal slot card
function MealSlotCard({ slot, recipe, onSwap, onClear, isSwapping, members, isHouseholdMode, compact = false }) {
  // ALL HOOKS MUST BE AT THE TOP - before any conditional returns
  const [isOverlayOpen, setIsOverlayOpen] = useState(false)
  const [swipeOffset, setSwipeOffset] = useState(0)
  const [isSwiping, setIsSwiping] = useState(false)
  const touchStartX = useRef(0)
  const touchStartY = useRef(0)
  const lastTapTime = useRef(0)

  // Calculate household portions for ingredient scaling
  const householdPortions = useMemo(() => {
    if (!isHouseholdMode || !members || members.length === 0 || !recipe) {
      return { total: 1, breakdown: [] }
    }

    const portions = members.map(member => {
      const portion = calculateMemberPortion(member, recipe, slot.id)
      return { name: member.name, portion, isPrimary: member.is_primary }
    })

    const total = portions.reduce((sum, p) => sum + p.portion, 0)

    const breakdown = portions.map(p => ({
      ...p,
      percentage: Math.round((p.portion / total) * 100)
    }))

    return { total, breakdown }
  }, [isHouseholdMode, members, recipe, slot.id])

  // Double-tap to open recipe overlay
  const handleDoubleTap = useCallback((e) => {
    // Don't open if tapping on interactive elements
    const isInteractive = e.target.closest('button, a, [role="button"]')
    if (isInteractive) return

    const now = Date.now()
    const timeSinceLastTap = now - lastTapTime.current

    if (timeSinceLastTap < 300 && timeSinceLastTap > 0) {
      // Double tap detected - open overlay
      setIsOverlayOpen(true)
      lastTapTime.current = 0
    } else {
      lastTapTime.current = now
    }
  }, [])

  // Swipe handlers - swipe LEFT to reveal Swap/Delete actions
  const handleTouchStart = useCallback((e) => {
    touchStartX.current = e.touches[0].clientX
    touchStartY.current = e.touches[0].clientY
    setIsSwiping(true)
    setSwipeOffset(0)
  }, [])

  const handleTouchMove = useCallback((e) => {
    if (!isSwiping) return
    const currentX = e.touches[0].clientX
    const currentY = e.touches[0].clientY
    const diffX = touchStartX.current - currentX // Positive = swipe left
    const diffY = Math.abs(currentY - touchStartY.current)

    // Only track horizontal swipe if more horizontal than vertical (and moved at least 10px)
    if (Math.abs(diffX) > 10 && Math.abs(diffX) > diffY) {
      setSwipeOffset(Math.max(0, Math.min(diffX, 100))) // Clamp 0-100
    }
  }, [isSwiping])

  const handleTouchEnd = useCallback(() => {
    // Swipe LEFT reveals actions, but snap to either 0 or 80px
    if (swipeOffset > 40) {
      setSwipeOffset(80) // Show actions
    } else {
      setSwipeOffset(0) // Hide actions
    }
    setIsSwiping(false)
  }, [swipeOffset])

  // Handle rating
  const handleRate = (rating) => {
    console.log('Recipe rated:', rating, recipe.id)
  }

  // Early return for empty slot - AFTER all hooks
  if (!recipe) {
    return (
      <div className={`border border-dashed rounded-lg flex items-center justify-between ${compact ? 'p-2.5' : 'p-4'}`}>
        <div className="flex items-center gap-2 text-muted-foreground">
          <span className={compact ? 'text-base' : 'text-xl'}>{slot.icon}</span>
          <span className={compact ? 'text-sm uppercase font-medium' : 'text-base'}>{slot.label}</span>
          <span className="text-xs text-muted-foreground/60">Â· {slot.time}</span>
        </div>
        <Button variant="ghost" size="sm" onClick={onSwap} className={compact ? 'h-7 text-xs' : ''}>
          Add
        </Button>
      </div>
    )
  }

  const macros = recipe.per_serve || {}

  // COMPACT MODE (mobile) - compact card with description
  // Double-tap to open recipe, swipe left to reveal Swap/Delete
  if (compact) {
    return (
      <>
        <div className="relative overflow-hidden rounded-lg h-full">
          {/* Action buttons behind the card (revealed on swipe) */}
          <div className="absolute right-0 top-0 bottom-0 flex items-stretch">
            <button
              onClick={() => { onSwap(); setSwipeOffset(0); }}
              className="w-10 bg-primary text-primary-foreground flex items-center justify-center text-xs font-medium"
            >
              Swap
            </button>
            <button
              onClick={() => { onClear(); setSwipeOffset(0); }}
              className="w-10 bg-destructive text-white flex items-center justify-center text-xs font-medium"
            >
              Del
            </button>
          </div>

          {/* Main card content - slides left to reveal actions */}
          <div
            className={`bg-card border rounded-lg h-full transition-transform ${isSwapping ? 'border-primary border-2' : ''}`}
            style={{
              transform: `translateX(${-swipeOffset}px)`,
              transition: isSwiping ? 'none' : 'transform 0.2s ease-out',
            }}
            onTouchStart={handleTouchStart}
            onTouchMove={handleTouchMove}
            onTouchEnd={handleTouchEnd}
            onClick={handleDoubleTap}
          >
            <div className="p-2 h-full flex flex-col justify-center">
              {/* Line 1: Icon + LUNCH + 12:00 + 10 min */}
              <div className="flex items-center gap-1 text-[11px] text-muted-foreground">
                <span className="text-sm">{slot.icon}</span>
                <span className="uppercase font-semibold tracking-wide">{slot.label}</span>
                <span className="text-muted-foreground/50">Â·</span>
                <span>{slot.time}</span>
                {recipe.total_time_mins && (
                  <>
                    <span className="text-muted-foreground/50">Â·</span>
                    <span>{recipe.total_time_mins}m</span>
                  </>
                )}
              </div>
              {/* Line 2: Meal title */}
              <h3 className="text-sm font-medium leading-tight line-clamp-1">
                {recipe.name}
              </h3>
              {/* Line 3: Description (single line with ellipsis) */}
              {recipe.description && (
                <p className="text-[11px] text-muted-foreground leading-tight line-clamp-1">
                  {recipe.description}
                </p>
              )}
            </div>
          </div>
        </div>

        {/* Full-screen recipe overlay */}
        <RecipeOverlay
          recipe={recipe}
          slot={slot}
          isOpen={isOverlayOpen}
          onClose={() => setIsOverlayOpen(false)}
          onRate={handleRate}
          householdPortions={householdPortions}
          isHouseholdMode={isHouseholdMode}
          onSwap={onSwap}
          onClear={onClear}
        />
      </>
    )
  }

  // FULL MODE (desktop)
  return (
    <>
      <div className={`relative border rounded-xl overflow-hidden ${isSwapping ? 'border-primary border-2' : ''}`}>
        {/* Main card content */}
        <div className="relative bg-card">
          <div className="p-4">
            {/* Meal type / time row */}
            <div className="flex items-center gap-2 text-xs text-muted-foreground mb-1.5">
              <span className="text-base">{slot.icon}</span>
              <span className="uppercase tracking-wide font-medium">{slot.label}</span>
              <span className="text-muted-foreground/50">â€¢</span>
              <span>{slot.time}</span>
              {recipe.total_time_mins && (
                <span className="flex items-center gap-1 ml-auto">
                  <Clock className="h-3.5 w-3.5" />
                  {recipe.total_time_mins} min
                </span>
              )}
            </div>

            {/* Recipe title */}
            <h3 className="text-base font-medium leading-tight">
              {recipe.name}
            </h3>

            {/* Description */}
            <p className="text-sm text-muted-foreground mt-1 leading-relaxed">
              {recipe.description}
            </p>

            {/* Macros */}
            <div className="flex gap-3 mt-2 text-xs text-muted-foreground">
              <span>{macros.calories} cal</span>
              <span>{macros.protein_g}P</span>
              <span>{macros.fat_g}F</span>
              <span>{macros.carbs_g}C</span>
            </div>
          </div>

          {/* Action buttons */}
          <div className="flex items-center gap-3 px-4 py-2 bg-muted/20 border-t">
            <button
              onClick={() => setIsOverlayOpen(true)}
              className="text-sm text-primary hover:underline"
            >
              View recipe
            </button>
            <span className="text-muted-foreground/30">|</span>
            <button
              onClick={onSwap}
              className={`text-sm hover:underline ${isSwapping ? 'text-primary font-medium' : 'text-muted-foreground'}`}
            >
              <RefreshCw className="h-3 w-3 inline mr-1" />
              Swap
            </button>
            <button
              onClick={onClear}
              className="text-sm text-muted-foreground hover:text-destructive hover:underline"
            >
              Remove
            </button>
          </div>
        </div>
      </div>

      {/* Full-screen recipe overlay */}
      <RecipeOverlay
        recipe={recipe}
        slot={slot}
        isOpen={isOverlayOpen}
        onClose={() => setIsOverlayOpen(false)}
        onRate={handleRate}
        householdPortions={householdPortions}
        isHouseholdMode={isHouseholdMode}
      />
    </>
  )
}

// Sub-component: Macro progress bar
function MacroProgress({ label, current, target, unit = '' }) {
  const percentage = target > 0 ? Math.min((current / target) * 100, 100) : 0
  const isOver = current > target
  
  return (
    <div>
      <div className="flex justify-between text-sm mb-1">
        <span className="text-muted-foreground">{label}</span>
        <span className={isOver ? 'text-amber-600 font-medium' : ''}>
          {Math.round(current)}{unit} / {target}{unit}
        </span>
      </div>
      <div className="h-2 bg-muted rounded-full overflow-hidden">
        <div 
          className={`h-full transition-all ${isOver ? 'bg-amber-500' : 'bg-primary'}`}
          style={{ width: `${percentage}%` }}
        />
      </div>
    </div>
  )
}
